/**
 * Escape_sequence_4
 */
public class Escape_sequence_4 {
    public static void main(String[] args) {
        System.out.println("Hello 'the fonty' "); // used single quote
        System.out.println("Hello \"the fonty\" "); // used double quote
        System.out.println("Hello \\the fonty\\ "); // used backslash
        System.out.println("Hello \nthe fonty "); // used new line
        System.out.println("Hello \tthe fonty "); // used tab
        System.out.println("Hello\bthe fonty "); // used backspace
    }
}